/*18 - Faça um algoritmo que receba um valor inteiro e imprima na tela a sua tabuada.*/

package Exercícios.devMagro;

public class exercicio18 {
    public static void main(String[] args) {
        int num = 8;

        for (int i=0; i<=10; i++){
            System.out.println(num + " X " + i + " = " + num * i);
        }
    }
}
